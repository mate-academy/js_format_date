'use strict';

/**
 *   Time flies, standards change. Let's get rid of the routine of changing the
 * date format. Create a `formatDate` function that accepts the `date` string,
 * the old `fromFormat` array and the new `toFormat` array. Function returns
 * given date in new format.
 *   The function can change a separator, reorder the date parts of convert a
 * data.year from 4 digits to 2 digits and back.
 *   When converting from YYYY to YY just use 2 last digit (1997 -> 97).
 *   When converting from YY to YYYY use 20YY if YY < 30 and 19YY otherwise.
 *
 * Examples:
 *
 * formatDate(
 *   '2020-02-18',
 *   ['YYYY', 'MM', 'DD', '-'],
 *   ['YYYY', 'MM', 'DD', '.'],
 * ) // '2020.02.18'
 *
 * formatDate(
 *   '2020-02-18',
 *   ['YYYY', 'MM', 'DD', '-'],
 *   ['DD', 'MM', 'YYYY', '.'],
 * ) // '18.02.2020'
 *
 * formatDate(
 *   '18-02-2020',
 *   ['DD', 'MM', 'YYYY', '-'],
 *   ['DD', 'MM', 'YY', '/'],
 * ) // '18/02/20'
 *
 * formatDate(
 *   '20/02/18',
 *   ['YY', 'MM', 'DD', '/'],
 *   ['YYYY', 'MM', 'DD', '.'],
 * ) // '2020.02.18'
 *
 * formatDate(
 *   '97/02/18',
 *   ['YY', 'MM', 'DD', '/'],
 *   ['DD', 'MM', 'YYYY', '.'],
 * ) // '18.02.1997'
 *
 * @param {string} date
 * @param {string[]} fromFormat
 * @param {string[]} toFormat
 *
 * @returns {string}
 */

function yearFormating(year) {
  return year >= 30 ? `19${year}` : `20${year}`;
}

function formatDate(date, fromFormat, toFormat) {
  // write code here
  const data = {
    day: '',
    month: '',
    year: '',
  };

  const separator = toFormat[toFormat.length - 1];

  const oldSeparator = fromFormat[fromFormat.length - 1];
  const currentDate = date.split(oldSeparator);
  const returnedValue = [];

  for (let i = 0; i < fromFormat.length - 1; i++) {
    switch (fromFormat[i]) {
      case 'DD':
        data.day = currentDate[i];
        break;
      case 'MM':
        data.month = currentDate[i];
        break;
      case 'YY':
      case 'YYYY':
        data.year = currentDate[i];
        break;
      default:
        return 'Wrong format!';
    }
  }

  for (let i = 0; i < toFormat.length - 1; i++) {
    switch (toFormat[i]) {
      case 'DD':
        returnedValue[i] = data.day;
        break;
      case 'MM':
        returnedValue[i] = data.month;
        break;
      case 'YY':
        returnedValue[i] = data.year.length === 4
          ? data.year.slice(-2)
          : data.year;
        break;
      case 'YYYY':
        returnedValue[i] = data.year.length === 4
          ? data.year
          : yearFormating(data.year);
        break;
      default:
        return 'Wrong format!';
    }
  }

  return returnedValue.join(separator);
}

module.exports = formatDate;
